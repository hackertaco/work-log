/**
 * Blob pathname for the LinkedIn import data document.
 * Stores the structured ProfileData (experience, education, skills, certifications)
 * parsed from a LinkedIn PDF profile export or data export ZIP.
 *
 * LinkedIn is used ONLY during initial onboarding (one-shot).
 * After the resume is bootstrapped, this file is retained as a source
 * reference but is NOT re-read by the system during normal operation.
 *
 * Document shape:
 *   {
 *     schemaVersion: 1,
 *     importedAt: ISO string,
 *     source: "linkedin_pdf" | "linkedin_zip",
 *     data: ProfileData            — structured sections from the LinkedIn export
 *   }
 *
 * ProfileData shape:
 *   {
 *     name: string | null,
 *     headline: string | null,
 *     about: string | null,
 *     location: string | null,
 *     profileImageUrl: string | null,
 *     experience: { title, company, duration, description }[],
 *     education: { school, degree, field, years }[],
 *     skills: string[],
 *     certifications: { name, issuer, date }[]
 *   }
 */
export const LINKEDIN_IMPORT_PATHNAME = "resume/linkedin-import.json";

/**
 * Check whether the resume document exists in Vercel Blob.
 *
 * Uses `list()` with an exact-prefix filter so that no full URL is
 * required — the pathname alone is sufficient.
 *
 * @returns {Promise<{ exists: false } | { exists: true, url: string, uploadedAt: string, size: number }>}
 */
export async function checkResumeExists() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: RESUME_DATA_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === RESUME_DATA_PATHNAME);

  if (!match) {
    return { exists: false };
  }

  return {
    exists: true,
    url: match.url,
    uploadedAt: match.uploadedAt,
    size: match.size
  };
}

/**
 * Save (overwrite) the living-resume JSON document in Vercel Blob.
 *
 * Uses `put()` with `addRandomSuffix: false` and `allowOverwrite: true`
 * so that the canonical pathname `resume/data.json` is always stable.
 *
 * @param {object} data  Structured resume document (RESUME_SCHEMA_VERSION)
 * @returns {Promise<{ url: string }>}
 */
export async function saveResumeData(data) {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  const json = JSON.stringify(data, null, 2);

  const result = await put(RESUME_DATA_PATHNAME, json, {
    access: "public",
    contentType: "application/json; charset=utf-8",
    addRandomSuffix: false,
    allowOverwrite: true,
    ...(token ? { token } : {})
  });

  return { url: result.url };
}

/**
 * Download and parse the living-resume JSON document from Vercel Blob.
 *
 * Returns `null` when no resume has been saved yet.
 *
 * @returns {Promise<object|null>}
 */
export async function readResumeData() {
  const status = await checkResumeExists();
  if (!status.exists) return null;

  const response = await fetch(status.url);
  if (!response.ok) {
    throw new Error(`Failed to fetch resume data: ${response.status}`);
  }
  const raw = await response.json();
  // Ensure all items carry a valid _source provenance tag.
  // migrateResumeDocument is idempotent and returns the same reference when
  // no migration is needed, so this adds zero overhead on modern documents.
  return migrateResumeDocument(raw);
}

