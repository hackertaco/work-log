// ─── Reconstruction marker ──────────────────────────────────────────────────────

/**
 * Write the reconstruction-needed marker to Vercel Blob.
 *
 * When this marker exists, the next GET /api/resume call with `?rebuild=1`
 * or a POST /api/resume/rebuild request will trigger a full reconstruction.
 *
 * @param {string} [reason="explicit"]  Human-readable reason for the rebuild request
 * @returns {Promise<void>}
 */
export async function markResumeForReconstruction(reason = "explicit") {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  const marker = JSON.stringify({
    markedAt: new Date().toISOString(),
    reason: String(reason)
  });

  await put(RECONSTRUCTION_MARKER_PATHNAME, marker, {
    access: "public",
    contentType: "application/json; charset=utf-8",
    addRandomSuffix: false,
    allowOverwrite: true,
    ...(token ? { token } : {})
  });
}

/**
 * Remove the reconstruction-needed marker from Vercel Blob.
 *
 * Called after a successful reconstruction so the marker is not
 * indefinitely re-triggered.
 *
 * @returns {Promise<void>}
 */
export async function clearReconstructionMarker() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  // Locate the marker to get its URL (del() needs the full URL).
  const { blobs } = await list({
    prefix: RECONSTRUCTION_MARKER_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === RECONSTRUCTION_MARKER_PATHNAME);
  if (!match) return; // Nothing to clear.

  try {
    await del(match.url, ...(token ? [{ token }] : []));
  } catch (err) {
    // Best-effort deletion; caller should not fail on this.
    console.warn("[blob] clearReconstructionMarker del failed:", err.message ?? String(err));
  }
}

/**
 * Check whether a reconstruction-needed marker exists in Vercel Blob.
 *
 * @returns {Promise<{ needsRebuild: false } | { needsRebuild: true, markedAt: string, reason: string }>}
 */
export async function checkReconstructionMarker() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: RECONSTRUCTION_MARKER_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === RECONSTRUCTION_MARKER_PATHNAME);
  if (!match) return { needsRebuild: false };

  // Fetch marker content for reason / timestamp.
  try {
    const response = await fetch(match.url);
    if (response.ok) {
      const data = await response.json();
      return {
        needsRebuild: true,
        markedAt: data.markedAt ?? match.uploadedAt,
        reason: data.reason ?? "explicit"
      };
    }
  } catch {
    // If we can't read the content, the marker still exists → signal rebuild.
  }

  return {
    needsRebuild: true,
    markedAt: match.uploadedAt,
    reason: "explicit"
  };
}

// ─── Resume snapshots ───────────────────────────────────────────────────────

/**
 * Derive a stable, filesystem-safe Blob pathname for a snapshot from a
 * given ISO timestamp (colons replaced with hyphens).
 *
 * @param {string} isoTimestamp  e.g. "2025-03-27T12:00:00.000Z"
 * @returns {string}             e.g. "resume/snapshots/2025-03-27T12-00-00.000Z.json"
 */
export function snapshotPathnameFor(isoTimestamp) {
  const safe = isoTimestamp.replace(/:/g, "-");
  return `${SNAPSHOTS_PREFIX}${safe}.json`;
}

/**
 * Save the current resume state as an immutable snapshot in Vercel Blob.
 *
 * Each snapshot is stored at a unique, timestamped pathname so it is never
 * overwritten.  The document structure wraps the resume in a metadata envelope:
 *
 *   {
 *     schemaVersion: 1,
 *     snapshotKey:   "resume/snapshots/{safe-timestamp}.json",
 *     snapshotAt:    ISO string,
 *     label:         string,             // human-readable reason (e.g. "approve")
 *     trigger:       string,             // machine action trigger (e.g. "approve")
 *     triggeredBy:   string,             // e.g. "approve", "patch", "rollback"
 *     resume:        { ...resumeDoc }
 *   }
 *
 * @param {object} resumeDoc           The living-resume document to snapshot
 * @param {{ label?: string, trigger?: string, triggeredBy?: string }} [meta]  Optional metadata
 * @returns {Promise<{ snapshotKey: string, url: string }>}
 */
export async function saveSnapshot(resumeDoc, meta = {}) {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  const now = new Date().toISOString();
  const pathname = snapshotPathnameFor(now);

  const envelope = {
    schemaVersion: 1,
    snapshotKey: pathname,
    snapshotAt: now,
    label: meta.label ?? "manual",
    trigger: meta.trigger ?? "unknown",
    triggeredBy: meta.triggeredBy ?? "unknown",
    resume: resumeDoc
  };

  const result = await put(pathname, JSON.stringify(envelope, null, 2), {
    access: "public",
    contentType: "application/json; charset=utf-8",
    addRandomSuffix: false,
    allowOverwrite: false,
    ...(token ? { token } : {})
  });

  return { snapshotKey: pathname, url: result.url };
}

/**
 * List all resume snapshots stored in Vercel Blob, sorted most-recent first.
 *
 * Each entry in the returned array includes:
 *   {
 *     snapshotKey: string,   // the pathname (usable in rollback requests)
 *     url: string,
 *     uploadedAt: string,    // ISO string from Blob metadata
 *     size: number
 *   }
 *
 * @returns {Promise<Array<{ snapshotKey: string, url: string, uploadedAt: string, size: number }>>}
 */
export async function listSnapshots() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: SNAPSHOTS_PREFIX,
    ...(token ? { token } : {})
  });

  return blobs
    .filter((b) => b.pathname.startsWith(SNAPSHOTS_PREFIX) && b.pathname.endsWith(".json"))
    .map((b) => ({
      snapshotKey: b.pathname,
      url: b.url,
      uploadedAt: b.uploadedAt,
      size: b.size
    }))
    .sort((a, b) => (a.uploadedAt < b.uploadedAt ? 1 : -1)); // most-recent first
}

/**
 * Read and parse a snapshot by its Blob pathname key.
 *
 * Returns the full snapshot envelope (including the nested `resume` field)
 * or `null` when the snapshot does not exist.
 *
 * @param {string} snapshotKey  e.g. "resume/snapshots/2025-03-27T12-00-00.000Z.json"
 * @returns {Promise<object|null>}  Snapshot envelope or null
 */
export async function readSnapshotByKey(snapshotKey) {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: snapshotKey,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === snapshotKey);
  if (!match) return null;

  const response = await fetch(match.url);
  if (!response.ok) {
    throw new Error(`Failed to fetch snapshot (${snapshotKey}): ${response.status}`);
  }
  return response.json();
}

// ─── LinkedIn import storage ─────────────────────────────────────────────────

/**
 * Save (overwrite) the LinkedIn import document in Vercel Blob.
 *
 * This is a one-shot write during onboarding: the caller passes the parsed
 * ProfileData together with its source tag.  The document is wrapped in a
 * metadata envelope for provenance tracking.
 *
 * Stored at `resume/linkedin-import.json` with `allowOverwrite: true` so
 * the user can re-import if the first upload was wrong.
 *
 * @param {object} profileData  Parsed ProfileData from parseLinkedInFile
 * @param {string} source       "linkedin_pdf" | "linkedin_zip"
 * @returns {Promise<{ url: string }>}
 */
export async function saveLinkedInImport(profileData, source) {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const envelope = {
    schemaVersion: 1,
    importedAt: new Date().toISOString(),
    source: String(source),
    data: profileData
  };

  const result = await put(LINKEDIN_IMPORT_PATHNAME, JSON.stringify(envelope, null, 2), {
    access: "public",
    contentType: "application/json; charset=utf-8",
    addRandomSuffix: false,
    allowOverwrite: true,
    ...(token ? { token } : {})
  });

  return { url: result.url };
}

/**
 * Download and parse the stored LinkedIn import document from Vercel Blob.
 *
 * Returns `null` when no LinkedIn import has been saved yet.
 *
 * @returns {Promise<{ schemaVersion: number, importedAt: string, source: string, data: object }|null>}
 */
export async function readLinkedInImport() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: LINKEDIN_IMPORT_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === LINKEDIN_IMPORT_PATHNAME);
  if (!match) return null;

  const response = await fetch(match.url);
  if (!response.ok) {
    throw new Error(`Failed to fetch LinkedIn import data: ${response.status}`);
  }
  return response.json();
}

/**
 * Delete the LinkedIn import document from Vercel Blob.
 *
 * No-op when no import document exists.
 * Exposed for administrative cleanup; not called in the normal flow.
 *
 * @returns {Promise<void>}
 */
export async function clearLinkedInImport() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: LINKEDIN_IMPORT_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === LINKEDIN_IMPORT_PATHNAME);
  if (!match) return;

  try {
    await del(match.url, ...(token ? [{ token }] : []));
  } catch (err) {
    console.warn("[blob] clearLinkedInImport del failed:", err.message ?? String(err));
  }
}

// ─── Strength keywords storage ─────────────────────────────────────────────────

/**
 * Save (overwrite) the strength-keywords document in Vercel Blob.
 *
 * Stores a StrengthKeywordsDocument at the stable pathname
 * `resume/strength-keywords.json` using `allowOverwrite: true` so that
 * repeated writes always update the same canonical file.
 *
 * The document shape is defined in resumeTypes.mjs (StrengthKeywordsDocument):
 *   {
 *     schemaVersion: 1,
 *     updatedAt: ISO string,
 *     source: "bootstrap" | "user" | "system",
 *     keywords: string[]
 *   }
 *
 * @param {import("./resumeTypes.mjs").StrengthKeywordsDocument} doc
 * @returns {Promise<{ url: string }>}
 */
export async function saveStrengthKeywords(doc) {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  const json = JSON.stringify(doc, null, 2);

  const result = await put(STRENGTH_KEYWORDS_PATHNAME, json, {
    access: "public",
    contentType: "application/json; charset=utf-8",
    addRandomSuffix: false,
    allowOverwrite: true,
    ...(token ? { token } : {})
  });

  return { url: result.url };
}

/**
 * Download and parse the strength-keywords document from Vercel Blob.
 *
 * Returns a default empty StrengthKeywordsDocument when no file has been
 * saved yet (i.e. before the first bootstrap).  This makes `readStrengthKeywords`
 * safe to call unconditionally — callers never need to handle a null result.
 *
 * Initialization contract:
 *   When the file does not exist, the returned document has:
 *     { schemaVersion: 1, updatedAt: <now>, source: "system", keywords: [] }
 *
 * @returns {Promise<import("./resumeTypes.mjs").StrengthKeywordsDocument>}
 */
export async function readStrengthKeywords() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;

  const { blobs } = await list({
    prefix: STRENGTH_KEYWORDS_PATHNAME,
    limit: 1,
    ...(token ? { token } : {})
  });

  const match = blobs.find((b) => b.pathname === STRENGTH_KEYWORDS_PATHNAME);
  if (!match) {
    // Return initialized empty document — no storage yet (pre-bootstrap state).
    return {
      schemaVersion: 1,
      updatedAt: new Date().toISOString(),
      source: "system",
      keywords: []
    };
  }

  const response = await fetch(match.url);
  if (!response.ok) {
    throw new Error(`Failed to fetch strength keywords: ${response.status}`);
  }
  return response.json();
}
