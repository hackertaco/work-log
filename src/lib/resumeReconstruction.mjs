/**
 * Full reconstruction pipeline: derive WorkLogExtract for every raw work-log
 * entry, **bypassing the extract cache**, and write each result back to the
 * cache (re-hydration).
 *
 * This is the "hard reset" path.  Unlike the normal candidate-generation
 * pipeline — which calls `readExtractCache` first and short-circuits on a HIT —
 * this function always calls the LLM for every entry so the cache is
 * guaranteed to contain fresh results after the run.
 *
 * The function NEVER throws.  Per-entry LLM failures are counted in
 * `result.failed` and logged as warnings so the pipeline always completes.
 *
 * Dependency-injection parameters (`extractFn`, `writeCacheFn`) allow tests
 * to stub the LLM and cache layers without `--experimental-test-module-mocks`.
 *
 * @param {Object}         options
 * @param {WorkLogEntry[]} options.workLogEntries  Raw entries from gatherWorkLogBullets()
 * @param {object|null}    options.currentResume   Current live resume doc (used for
 *                                                 deduplication context in the LLM diff step).
 *                                                 May be null if no resume exists yet.
 * @param {Function}       [options.extractFn]     Injection: (workLogSummary, resume) → Promise<WorkLogExtract>
 *                                                 Defaults to extractResumeUpdatesFromWorkLog
 * @param {Function}       [options.writeCacheFn]  Injection: (date, extract) → Promise<void>
 *                                                 Defaults to writeExtractCache
 * @returns {Promise<ReconstructionStats>}
 *
 * @typedef {Object} ReconstructionStats
 * @property {number}   total     Total entries attempted
 * @property {number}   processed Entries where LLM extraction succeeded and cache was written
 * @property {number}   failed    Entries where LLM extraction threw an error
 * @property {number}   skipped   Entries without a valid date (defensive guard)
 * @property {string[]} dates     Dates successfully processed (ascending order)
 */
export async function fullReconstructExtractCache({
  workLogEntries,
  currentResume = null,
  extractFn = extractResumeUpdatesFromWorkLog,
  writeCacheFn = writeExtractCache
}) {
  const entries = Array.isArray(workLogEntries) ? workLogEntries : [];

  let processed = 0;
  let failed = 0;
  let skipped = 0;
  const dates = [];

  for (const entry of entries) {
    const { date } = entry;

    // Guard: skip entries with no valid date string
    if (!date || typeof date !== "string" || !date.trim()) {
      skipped++;
      continue;
    }

    // Adapt the raw WorkLogEntry into the shape expected by extractResumeUpdatesFromWorkLog.
    //
    // gatherWorkLogBullets returns:
    //   { date, candidates, companyCandidates, openSourceCandidates }
    //
    // extractResumeUpdatesFromWorkLog / buildWorkLogDiff reads:
    //   workLog.resume.candidates
    //   workLog.resume.companyCandidates
    //   workLog.resume.openSourceCandidates
    //
    // Wrapping under `resume` aligns the two interfaces without modifying either.
    const workLogSummary = {
      date,
      resume: {
        candidates:           Array.isArray(entry.candidates)           ? entry.candidates           : [],
        companyCandidates:    Array.isArray(entry.companyCandidates)    ? entry.companyCandidates    : [],
        openSourceCandidates: Array.isArray(entry.openSourceCandidates) ? entry.openSourceCandidates : []
      }
    };

    try {
      // ── Bypass cache: call extractFn directly (no readExtractCache) ──────────
      const extract = await extractFn(workLogSummary, currentResume);

      // ── Re-hydrate cache: write fresh result ─────────────────────────────────
      await writeCacheFn(date, extract);

      processed++;
      dates.push(date);
    } catch (err) {
      console.warn(
        `[resumeReconstruction/fullReconstructExtractCache] extract failed for date=${date}:`,
        err.message ?? String(err)
      );
      failed++;
    }
  }

  // Return dates in ascending chronological order (entries are already sorted
  // ascending from gatherWorkLogBullets, but re-sort defensively).
  dates.sort();

  return {
    total: entries.length,
    processed,
    failed,
    skipped,
    dates
  };
}

