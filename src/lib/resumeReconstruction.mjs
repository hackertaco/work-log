/**
 * Reconstruct a complete resume document from the original PDF text and
 * accumulated work-log bullet candidates via a single LLM call.
 *
 * When OpenAI is unavailable (missing key or disabled), the function returns
 * the current resume unchanged rather than throwing, so the caller can
 * degrade gracefully.
 *
 * @param {Object} input
 * @param {string}         input.pdfText          Plain text from the original PDF
 *                                                 (stored in resume/pdf-text.txt).
 * @param {WorkLogEntry[]} input.workLogEntries    Entries from gatherWorkLogBullets().
 * @param {object}         input.currentResume     The current live resume document
 *                                                 (for context and user-edit preservation).
 * @returns {Promise<ReconstructionResult>}
 *
 * @typedef {Object} ReconstructionResult
 * @property {object}   resumeData       Freshly reconstructed resume document
 * @property {string[]} strengthKeywords Updated strength keywords
 * @property {object[]} displayAxes      Updated display axes
 */
export async function reconstructResumeFromSources(input) {
  const { pdfText, workLogEntries, currentResume } = input;

  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) {
    throw new Error(
      "OPENAI_API_KEY is not set — cannot reconstruct resume from sources"
    );
  }
  if (process.env.WORK_LOG_DISABLE_OPENAI === "1") {
    throw new Error(
      "OpenAI integration is disabled (WORK_LOG_DISABLE_OPENAI=1)"
    );
  }

  const payload = buildReconstructionPayload({ pdfText, workLogEntries, currentResume });

  const response = await fetch(OPENAI_URL, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${apiKey}`
    },
    body: JSON.stringify(payload)
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(
      `Reconstruction LLM call failed: ${response.status} ${errorText.slice(0, 400)}`
    );
  }

  const data = await response.json();
  const rawText = data.output_text || extractOutputText(data);
  if (!rawText) {
    throw new Error("Reconstruction LLM call returned empty output");
  }

  const parsed = JSON.parse(rawText);
  return normalizeReconstructionResult(parsed, currentResume);
}

