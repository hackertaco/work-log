// Public: staleness check
// ---------------------------------------------------------------------------

/**
 * Determine whether the stored resume is stale relative to available work logs.
 *
 * A resume is considered stale when:
 *   1. Its rebuild/generation timestamp is older than STALE_THRESHOLD_DAYS and
 *      there is at least one work-log entry newer than that timestamp, OR
 *   2. The resume has never been rebuilt (`meta.rebuiltAt` is absent) and
 *      work-log entries exist that post-date `meta.generatedAt`.
 *
 * @param {object}        resume        The stored resume document.
 * @param {WorkLogEntry[]} workLogEntries  Entries from gatherWorkLogBullets().
 * @returns {{ isStale: boolean, latestLogDate: string|null, checkpointDate: string|null }}
 */
export function isResumeStale(resume, workLogEntries) {
  if (!Array.isArray(workLogEntries) || workLogEntries.length === 0) {
    return { isStale: false, latestLogDate: null, checkpointDate: null };
  }

  // Latest date among gathered work logs
  const sortedDates = workLogEntries.map((e) => e.date).sort();
  const latestLogDate = sortedDates[sortedDates.length - 1];

  // The reference timestamp to compare against (rebuiltAt preferred over generatedAt)
  const checkpointIso =
    resume?.meta?.rebuiltAt ?? resume?.meta?.generatedAt ?? null;

  if (!checkpointIso) {
    // No timestamp at all — treat as stale when any work logs exist
    return { isStale: true, latestLogDate, checkpointDate: null };
  }

  const checkpointDate = checkpointIso.slice(0, 10); // YYYY-MM-DD

  // Is the latest log file newer than the last checkpoint?
  if (latestLogDate > checkpointDate) {
    // Additional guard: only flag as stale if the checkpoint itself is older
    // than the threshold (prevents false-positives on same-day batches).
    const checkpointMs = new Date(checkpointIso).getTime();
    const nowMs = Date.now();
    const ageMs = nowMs - checkpointMs;
    const thresholdMs = STALE_THRESHOLD_DAYS * 24 * 60 * 60 * 1000;

    if (ageMs >= thresholdMs) {
      return { isStale: true, latestLogDate, checkpointDate };
    }
  }

  return { isStale: false, latestLogDate, checkpointDate };
}

/**
 * Build layered projection hints for the reconstruction prompt.
 * These hints are derived from shared evidence bundles so the LLM sees
 * the intended separation between project-scale experience facts and
 * person/strength signals before synthesizing resume text.
 *
 * @param {object[]} workLogEntries
 * @returns {{ projectHints: string[], personSignalHints: string[] }}
 */
export function buildLayeredProjectionHints(workLogEntries) {
  const bundles = buildEvidenceBundles(Array.isArray(workLogEntries) ? workLogEntries : []);
  const projectCandidates = projectExperienceCandidatesFromBundles(bundles);
  const personSignals = personSignalsFromBundles(bundles).filter((signal) => signal.surfaced);

  return {
    projectHints: projectCandidates.slice(0, 6).map((candidate) => {
      const dates = candidate.activeDates.length > 1
        ? `${candidate.activeDates[0]} to ${candidate.activeDates[candidate.activeDates.length - 1]}`
        : (candidate.activeDates[0] || "recent");
      return `${candidate.repo}: ${candidate.totalCommits} commits across ${dates}. ${candidate.summary}`;
    }),
    personSignalHints: personSignals.slice(0, 5).map((signal) => {
      const examples = signal.evidenceExamples.length > 0
        ? ` 예: ${signal.evidenceExamples.join(" / ")}`
        : "";
      return `${signal.label}: score=${signal.score}, repetition=${signal.repetition}, confidence=${signal.confidence}.${examples}`;
    }),
  };
}

