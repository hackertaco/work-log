/**
 * Resume Reconstruction — Full rebuild from original sources.
 *
 * When the cached resume document is missing, stale, or explicitly invalidated,
 * this module rebuilds all resume bullets from:
 *   1. The original PDF plain text stored in Vercel Blob at bootstrap time
 *   2. Accumulated daily work-log bullet candidates (data/daily/*.json on disk)
 *
 * The LLM is called with the combined source material to regenerate a fully
 * structured resume.  User-edited fields (sections where _source / _sources
 * is "user") are preserved and layered on top of the reconstructed result so
 * that user edits always win over system merges.
 *
 * Key functions:
 *   gatherWorkLogBullets(dataDir)        — scan daily JSON files on disk
 *   reconstructResumeFromSources(input)  — LLM call → BootstrapResult
 *   mergeWithUserEdits(current, fresh)   — preserve user edits
 *   isResumeStale(resume, workLogDates)  — staleness predicate
 *
 * Environment variables:
 *   OPENAI_API_KEY            — required for LLM call
 *   WORK_LOG_OPENAI_URL       — optional endpoint override
 *   WORK_LOG_OPENAI_MODEL     — optional model override (default: gpt-5.4-mini)
 *   WORK_LOG_DISABLE_OPENAI   — set "1" to disable (throws)
 *   RESUME_STALE_DAYS         — days before resume is considered stale (default: 7)
 */

import fs from "node:fs/promises";
import path from "node:path";

import { fileExists } from "./utils.mjs";
import { extractResumeUpdatesFromWorkLog } from "./resumeWorkLogExtract.mjs";
