const OPENAI_URL =
  process.env.WORK_LOG_OPENAI_URL || "https://api.openai.com/v1/responses";
const OPENAI_MODEL = process.env.WORK_LOG_OPENAI_MODEL || "gpt-5.4-mini";

/** How many days old a resume must be (without a newer work log) to be stale. */
const STALE_THRESHOLD_DAYS = Math.max(
  1,
  parseInt(process.env.RESUME_STALE_DAYS || "7", 10) || 7
);

// ---------------------------------------------------------------------------
// Public: work-log scanning
// ---------------------------------------------------------------------------

/**
 * Scan the `data/daily/` directory and collect resume bullet candidates from
 * every available daily work-log JSON file.
 *
 * Each returned entry corresponds to one work-log day and contains the raw
 * bullet strings that were generated by the batch pipeline.  The caller can
 * filter or limit these entries before passing them to the LLM.
 *
 * @param {string} dataDir  Absolute path to the `data/` directory
 *                          (from config.dataDir).
 * @returns {Promise<WorkLogEntry[]>}
 *
 * @typedef {Object} WorkLogEntry
 * @property {string}   date                YYYY-MM-DD
 * @property {string[]} candidates          Combined candidate bullets
 * @property {string[]} companyCandidates   Company-project bullets
 * @property {string[]} openSourceCandidates Open-source project bullets
 */
export async function gatherWorkLogBullets(dataDir) {
  const dailyDir = path.join(dataDir, "daily");
  if (!(await fileExists(dailyDir))) return [];

  let entries;
  try {
    entries = await fs.readdir(dailyDir);
  } catch {
    return [];
  }

  const jsonFiles = entries
    .filter((name) => /^\d{4}-\d{2}-\d{2}\.json$/.test(name))
    .sort(); // ascending order

  const result = [];

  for (const filename of jsonFiles) {
    const date = filename.replace(/\.json$/, "");
    const filePath = path.join(dailyDir, filename);
    try {
      const raw = await fs.readFile(filePath, "utf8");
      const parsed = JSON.parse(raw);
      const resumeSection = parsed.resume;
      if (!resumeSection) continue;

      result.push({
        date,
        candidates: Array.isArray(resumeSection.candidates)
          ? resumeSection.candidates.filter((s) => typeof s === "string" && s.trim())
          : [],
        companyCandidates: Array.isArray(resumeSection.companyCandidates)
          ? resumeSection.companyCandidates.filter((s) => typeof s === "string" && s.trim())
          : [],
        openSourceCandidates: Array.isArray(resumeSection.openSourceCandidates)
          ? resumeSection.openSourceCandidates.filter((s) => typeof s === "string" && s.trim())
          : []
      });
    } catch {
      // Skip unparseable files; reconstruction continues with remaining data.
    }
  }

  return result;
}

// ---------------------------------------------------------------------------
// Public: staleness check
// ---------------------------------------------------------------------------

/**
 * Determine whether the stored resume is stale relative to available work logs.
 *
 * A resume is considered stale when:
 *   1. Its rebuild/generation timestamp is older than STALE_THRESHOLD_DAYS and
 *      there is at least one work-log entry newer than that timestamp, OR
 *   2. The resume has never been rebuilt (`meta.rebuiltAt` is absent) and
 *      work-log entries exist that post-date `meta.generatedAt`.
 *
 * @param {object}        resume        The stored resume document.
 * @param {WorkLogEntry[]} workLogEntries  Entries from gatherWorkLogBullets().
 * @returns {{ isStale: boolean, latestLogDate: string|null, checkpointDate: string|null }}
 */
export function isResumeStale(resume, workLogEntries) {
  if (!Array.isArray(workLogEntries) || workLogEntries.length === 0) {
    return { isStale: false, latestLogDate: null, checkpointDate: null };
  }

  // Latest date among gathered work logs
  const sortedDates = workLogEntries.map((e) => e.date).sort();
  const latestLogDate = sortedDates[sortedDates.length - 1];

  // The reference timestamp to compare against (rebuiltAt preferred over generatedAt)
  const checkpointIso =
    resume?.meta?.rebuiltAt ?? resume?.meta?.generatedAt ?? null;

  if (!checkpointIso) {
    // No timestamp at all — treat as stale when any work logs exist
    return { isStale: true, latestLogDate, checkpointDate: null };
  }

  const checkpointDate = checkpointIso.slice(0, 10); // YYYY-MM-DD

  // Is the latest log file newer than the last checkpoint?
  if (latestLogDate > checkpointDate) {
    // Additional guard: only flag as stale if the checkpoint itself is older
    // than the threshold (prevents false-positives on same-day batches).
    const checkpointMs = new Date(checkpointIso).getTime();
    const nowMs = Date.now();
    const ageMs = nowMs - checkpointMs;
    const thresholdMs = STALE_THRESHOLD_DAYS * 24 * 60 * 60 * 1000;

    if (ageMs >= thresholdMs) {
      return { isStale: true, latestLogDate, checkpointDate };
    }
  }

  return { isStale: false, latestLogDate, checkpointDate };
}

