/**
 * Merge a freshly reconstructed resume with the current live document,
 * ensuring that every user-edited field is preserved unchanged.
 *
 * Merge rules:
 *   • `summary`         — preserved when `_sources.summary === "user"` or `"user_approved"`
 *   • `contact`         — preserved when `_sources.contact === "user"` or `"user_approved"`
 *   • `skills`          — preserved when `_sources.skills === "user"` or `"user_approved"`
 *   • `experience[i]`   — preserved entirely when `_source === "user"` or `"user_approved"`;
 *                         system entries are replaced with the fresh reconstruction
 *   • `education`       — always kept from the current resume (structural, not
 *                         derived from work logs)
 *   • `projects`        — user-edited/approved entries preserved; new system entries added
 *   • `certifications`  — always kept from the current resume
 *   • `meta`            — current bootstrapped metadata preserved; `rebuiltAt`
 *                         is stamped to the current time
 *
 * @param {object} current      The existing live resume document (may have user edits)
 * @param {ReconstructionResult} reconstructionResult  Result from reconstructResumeFromSources()
 * @returns {object}  The merged resume document
 */
export function mergeWithUserEdits(current, { resumeData, strengthKeywords, displayAxes }) {
  const fresh = resumeData ?? {};
  const cur = current ?? {};

  // ── meta: preserve original bootstrap metadata, stamp rebuiltAt ───────────
  const mergedMeta = {
    ...(fresh.meta ?? {}),
    // Critical provenance fields from the original bootstrap
    language:      cur.meta?.language    ?? fresh.meta?.language    ?? "en",
    source:        cur.meta?.source      ?? fresh.meta?.source      ?? "pdf",
    generatedAt:   cur.meta?.generatedAt ?? fresh.meta?.generatedAt ?? new Date().toISOString(),
    schemaVersion: cur.meta?.schemaVersion ?? 1,
    pdf_name:      cur.meta?.pdf_name    ?? fresh.meta?.pdf_name    ?? null,
    linkedin_url:  cur.meta?.linkedin_url ?? fresh.meta?.linkedin_url ?? null,
    // Reconstruction timestamp
    rebuiltAt: new Date().toISOString()
  };

  // ── _sources: start with fresh, then overlay user overrides ───────────────
  const curSrc = cur._sources ?? {};
  const freshSrc = fresh._sources ?? {};
  const mergedSources = { ...freshSrc };

  // Helper: returns true when a source value represents human-confirmed content
  // (either directly authored or explicitly approved via the suggestions UI).
  const isHumanConfirmed = (src) => src === "user" || src === "user_approved";

  // ── Scalar sections: preserve user-edited/approved versions ───────────────
  const mergedSummary =
    isHumanConfirmed(curSrc.summary) ? cur.summary : (fresh.summary ?? cur.summary ?? "");
  if (isHumanConfirmed(curSrc.summary)) mergedSources.summary = curSrc.summary;

  const mergedContact =
    isHumanConfirmed(curSrc.contact) ? { ...cur.contact } : (fresh.contact ?? cur.contact);
  if (isHumanConfirmed(curSrc.contact)) mergedSources.contact = curSrc.contact;

  const mergedSkills =
    isHumanConfirmed(curSrc.skills)
      ? { ...cur.skills }
      : (fresh.skills ?? cur.skills ?? { technical: [], languages: [], tools: [] });
  if (isHumanConfirmed(curSrc.skills)) mergedSources.skills = curSrc.skills;

  // ── Experience: user-edited/approved entries win; system entries refreshed ─
  const curExp = Array.isArray(cur.experience) ? cur.experience : [];
  const freshExp = Array.isArray(fresh.experience) ? fresh.experience : [];

  const userExp = curExp.filter((e) => isHumanConfirmed(e._source));
  const userCompanies = new Set(
    userExp.map((e) => (e.company ?? "").toLowerCase().trim()).filter(Boolean)
  );
  // Keep system entries from the fresh reconstruction that don't clash with
  // user-edited/approved entries.
  const systemExp = freshExp.filter(
    (e) => !userCompanies.has((e.company ?? "").toLowerCase().trim())
  );
  const mergedExperience = [...userExp, ...systemExp];

  // ── Education: static — always keep current (not derived from work logs) ──
  const mergedEducation =
    Array.isArray(cur.education) && cur.education.length > 0
      ? cur.education
      : (fresh.education ?? []);

  // ── Projects: user-edited/approved entries win; new system entries appended ─
  const curProj = Array.isArray(cur.projects) ? cur.projects : [];
  const freshProj = Array.isArray(fresh.projects) ? fresh.projects : [];
  const userProj = curProj.filter((p) => isHumanConfirmed(p._source));
  const userProjNames = new Set(
    userProj.map((p) => (p.name ?? "").toLowerCase().trim()).filter(Boolean)
  );
  const systemProj = freshProj.filter(
    (p) => !userProjNames.has((p.name ?? "").toLowerCase().trim())
  );
  const mergedProjects =
    userProj.length > 0 ? [...userProj, ...systemProj] : (freshProj.length > 0 ? freshProj : curProj);

  // ── Certifications: static — always keep current ──────────────────────────
  const mergedCertifications =
    Array.isArray(cur.certifications) && cur.certifications.length > 0
      ? cur.certifications
      : (fresh.certifications ?? []);

  const mergedResult = {
    meta: mergedMeta,
    _sources: mergedSources,
    contact: mergedContact,
    summary: mergedSummary,
    experience: mergedExperience,
    education: mergedEducation,
    skills: mergedSkills,
    projects: mergedProjects,
    certifications: mergedCertifications,
    strength_keywords: Array.isArray(strengthKeywords) ? strengthKeywords : (cur.strength_keywords ?? []),
    display_axes: Array.isArray(displayAxes) ? displayAxes : (cur.display_axes ?? [])
  };

  // ── Automatic quality tracking for reconstructed bullets ──────────────────
  // Compare old system-generated bullets against their fresh reconstructions.
  // Only track system-sourced bullets (user bullets are preserved, not compared).
  // Fire-and-forget: errors are logged but never block the merge.
  _trackReconstructionBulletQuality(cur, fresh).catch((err) =>
    console.warn("[resumeReconstruction/mergeWithUserEdits] quality tracking failed (non-fatal):", err)
  );

  return mergedResult;
}

/**
 * Compare system-generated bullets from the previous resume against the
 * freshly reconstructed bullets.  Tracks how much the reconstruction changed
 * each bullet, enabling quality monitoring of the reconstruction pipeline.
 *
 * Only compares bullets from items whose _source is NOT user/user_approved
 * (user-edited items are preserved as-is and don't participate in
 * reconstruction quality measurement).
 *
 * @param {object} previous — the resume before reconstruction
 * @param {object} fresh    — the newly reconstructed resume
 * @returns {Promise<void>}
 */
async function _trackReconstructionBulletQuality(previous, fresh) {
  const isHumanConfirmed = (src) => src === "user" || src === "user_approved";
  const pairs = [];

  for (const section of ["experience", "projects"]) {
    const oldItems = Array.isArray(previous?.[section]) ? previous[section] : [];
    const newItems = Array.isArray(fresh?.[section]) ? fresh[section] : [];

    // Match items by normalized company/name
    const keyFn = section === "experience"
      ? (item) => (item.company ?? "").toLowerCase().trim()
      : (item) => (item.name ?? item.title ?? "").toLowerCase().trim();

    const newItemMap = new Map();
    for (const item of newItems) {
      const key = keyFn(item);
      if (key) newItemMap.set(key, item);
    }

    for (const oldItem of oldItems) {
      // Skip user-edited items (they're preserved, not reconstructed)
      if (isHumanConfirmed(oldItem._source)) continue;

      const key = keyFn(oldItem);
      const newItem = newItemMap.get(key);
      if (!newItem) continue;

      const oldBullets = Array.isArray(oldItem.bullets) ? oldItem.bullets : [];
      const newBullets = Array.isArray(newItem.bullets) ? newItem.bullets : [];

      // Compare by index position
      const maxLen = Math.max(oldBullets.length, newBullets.length);
      for (let j = 0; j < maxLen; j++) {
        const oldText = oldBullets[j];
        const newText = newBullets[j];

        if (typeof oldText === "string" && typeof newText === "string" && oldText !== newText) {
          pairs.push({
            generatedText: oldText,
            finalText: newText,
            action: "edited",
            section,
            logDate: null,
          });
        } else if (typeof oldText === "string" && newText === undefined) {
          pairs.push({
            generatedText: oldText,
            finalText: oldText,
            action: "discarded",
            section,
            logDate: null,
          });
        }
      }
    }
  }

  if (pairs.length > 0) {
    console.info(`[resumeReconstruction] tracking ${pairs.length} bullet changes from reconstruction`);
    await trackBulletEditBatch(pairs, { useEmbeddings: false });
  }
}

